
namespace DataEditorX.Common
{
    /// <summary>
    /// Rectangle area.
    /// </summary>
    public class Area
    {
        public Area()
        {
            left = 0;
            top = 0;
            width = 0;
            height = 0;
        }
        /// <summary>
        /// Left position.
        /// </summary>
        public int left;
        /// <summary>
        /// Top position.
        /// </summary>
        public int top;
        /// <summary>
        /// Width.
        /// </summary>
        public int width;
        /// <summary>
        /// Height.
        /// </summary>
        public int height;
    }
}
